                                                    </li>
                                                </ul>
                                            </td>
                                    </tr>

                                    <tr>
                                        <td>3</td>
                                        <td>Nutrición, salud y su evaluación</td>
                                        <td id="contTablaIzq">Relacionar los conceptos básicos de nutrición con la biodiversidad, la seguridad alimentaria y la salud
                                        </td>
                                        <td id="contTablaIzq">

                                            <ul class="vineta">
                                                <li>Clases presenciales del profesor con apoyo de material visual y/o audiovisual, que describa y ejemplifique los conceptos analizados.</li>
                                                <li>Permitir la exposición de conceptos empíricos o investigados como parte de tareas, hasta la construcción del concepto formal.</li>
                                                <li>El alumno elaborará un instrumento para medir diversidad dietética con base en un estudio de caso.
                                                </li>
                                            </ul>
                                        </td>
                                    </tr>

