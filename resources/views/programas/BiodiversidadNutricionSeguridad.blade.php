                                        </td>
                                            <td id="contTablaIzq">
                                                <ul class="vineta">
                                                    <li>Exposición por parte del profesor con apoyo de material visual y/o audiovisual, que describa y ejemplifique los conceptos analizados, apoyándose en libros y artículos.</li>
                                                    <li>Lecturas complementarias posteriores a cada tema, para concretar conceptos y reforzar conocimientos.</li>
