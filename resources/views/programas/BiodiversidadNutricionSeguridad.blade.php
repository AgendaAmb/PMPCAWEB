                                        <td>Biodiversidad: su conservación y aprovechamiento</td>
                                        <td id="contTablaIzq"> Conocer los niveles de biodiversidad y la importancia de la diversidad genética. Identificar la implicación de la biodiversidad con la diversidad dietética de la población y los aportes de la biodiversidad a la seguridad alimentaria. Conocer la aplicación de los índices de biodiversidad útiles para la seguridad alimentaria.
                                        </td>
                                        <td id="contTablaIzq">
