                                        <td>La seguridad alimentaria, sus componentes y evaluación</td>
                                        <td id="contTablaIzq">Conocer el concepto de seguridad alimentaria e identificar sus componentes. Revisar y discutir las herramientas e instrumentos más empleados en la evaluación de cada componente de la seguridad alimentaria. Identificar a las organizaciones en el mundo y en México relacionadas con la evaluación de la seguridad alimentaria.
                                        </td>
                                        <td id="contTablaIzq">

                                            <ul class="vineta">
                                                <li>Exposición por parte del profesor con apoyo de material visual y/o audiovisual, que describa y ejemplifique los conceptos analizados, apoyándose en libros y artículos.</li>
                                                <li>El alumno elaborará de mapas conceptuales para identificar las ventajas y las desventajas de los instrumentos para evaluar seguridad alimentaria revisados en clase.</li>
                                                <li>El alumno elaborará de un instrumento para evaluar seguridad alimentaria a partir de un estudio de caso.
                                                </li>
                                            </ul>
                                        </td>
                                    </tr>

                                    <tr>
