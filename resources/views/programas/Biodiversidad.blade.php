                <h4> <strong>Objetivo general de aprendizaje</strong> </h4>
                <p style="text-align: justify;"><br>
                    Comprender los conceptos que relacionan a la biodiversidad y la producción de los alimentos con la nutrición y la seguridad alimentaria de la población. El alumno identificará los componentes que determinan la seguridad alimentaria, así como los indicadores e instrumentos que la evalúan. Al finalizar el curso el alumno tendrá los conocimientos básicos que le permitirán analizar la seguridad alimentaria desde diferentes escenarios y situaciones.<br>
                </p>
