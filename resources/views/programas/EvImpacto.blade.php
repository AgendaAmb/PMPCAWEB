                <h4> <strong>Objetivo general de aprendizaje</strong> </h4>
                <p>Al término del curso, el estudiante comprenderá y aplicará metodologías para evaluar los impactos ambientales generados por la preparación, construcción, operación o desarrollo y abandono o cierre de obras o actividades; y conocerá los principios para la realización de estudios de riesgo ambiental de actividades riesgosas y altamente riesgosas, según la normativa aplicable.</p>
