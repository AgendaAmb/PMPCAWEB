                        <h4><strong>Evaluación</strong></h4>
                        <p align="justify">Al final del curso el (la) alumno (a) deberá entregar un ensayo escrito enfocado al análisis o crítica de alguno, varios o todos los textos analizados, o bien tomarlos como referente para articular el propio trabajo de investigación o reflexión personal. Con el propósito de recibir orientación del instructor, los (las) estudiantes deberán entregar <b>dos avances en el primer y segundo tercio</b> de la duración del curso.<b>la entrega del ensayo final</b>, improrrogable</p>
                        <p>Los <b>criterios</b> a considerar en la evaluación del ensayo son los siguientes: </p>
                        <ul class="vineta">
                            <li>Redacción (puntuación, ortografía, acentuación, sintaxis, uso correcto de las palabras, concordancias de género y número, párrafos de tamaño adecuado, uso correcto de mayúsculas y minúsculas,etc.);
                            </li>
                            <li>Consistencia (ilación, concordancia entre el título y el contenido del ensayo, adecuada integración “orgánica” o articulación entre las partes del ensayo, acotamiento adecuado de los temas abordados,etc.);
                            </li>
                            <li>Incorporación de todas las partes del ensayo: introducción, cuerpo del ensayo, discusión, conclusiones, referencias;
                            </li>
                            <li>Aparato crítico: registro adecuado de citas y referencias, uso y registro de todas las referencias de soporte del ensayo;
                            </li>
                            <li>Suficiencia de información de soporte (revisión bibliográfica, tanto de los materiales del curso como de bibliografía adicional);
                            </li>
                            <li>Originalidad de la interpretación, uso creativo del lenguaje, discusión de la información del cuerpo del ensayo o en la defensa de las tesis expuestas;
                            </li>
                            <li>Uso adecuado y correcto de materiales bibliográficos del internet –registro de fuentes y referencias de información;
                            </li>
                            <li>Material citado o traducido de otras lenguas de manera correcta;
                            </li>
                            <li>Edición pulcra, detallada y cuidadosa de la versión final del texto (poner una carátula y un índice, eliminar “errores de dedo”, muletillas, cacofonías, “viudas”, “huérfanos”, ideas incompletas o fragmentarias, etc.).
                            </li>
                        </ul>
