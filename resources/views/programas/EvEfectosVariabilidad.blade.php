                <h4> <strong>Objetivo general de aprendizaje</strong> </h4>
                <div class="table-responsive" id="contTablaIzq">
                            <table class="table table-bordered border-dark" id="colorTexto">
                                <tr>
                                    <td id="contTabla"><strong>Objetivo general</strong></td>

                                    <td>Comprender las bases de la dinámica del sistema climático que determinan la variabilidad y el cambio climático, e identificar los impactos más notables producidos por estos fenómenos en los sistemas de producción agrícolas, pecuarios y forestales, en México.</td>
                                </tr>

                                <tr>
                                    <td id="contTabla"><strong>Objetivos Específicos</strong></td>

                                    <td>Entender el marco conceptual de la Evaluación de Riesgos provocados por la variabilidad y el cambio climático en los sistemas de producción agrícolas y pecuarios. Dilucidar las características de los diferentes tipos de modelos de evaluación de riesgos, y su importancia, para entender los cambios y modificaciones en su dinámica producidos por la interacción entre las amenazas, la exposición y la vulnerabilidad. Entender los procedimientos de construcción de escenarios para la construcción de estrategias de adaptación de los sistemas de producción de alimentos a la variabilidad y el cambio climático, particularmente, para las zonas desérticas. Comprender las bases para el diseño de sistemas de monitorización, pronóstico y alerta temprana de los fenómenos meteorológico-climáticos y sus impactos en los sistemas de producción agrícolas, a nivel regional y local.</td>
