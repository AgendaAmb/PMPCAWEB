                <p>El curso cuenta con un espacio virtual de trabajo vía Internet en el sitio: que utiliza la tecnología Microsoft SharePoint, diseñada para equipos de trabajo que requieren compartir información. Contará con las siguientes secciones, disponibles para todos los estudiantes:</p>
                <ul class="vineta">
                    <li>Bibliotecas de documentos</li>
                    <li>Tableros de anuncios, eventos y tareas</li>
                    <li>Foros de discusión</li>
                    <li>Lista de contactos</li>
                    <li>Enlaces (vínculos) a páginas de Internet sobre temas específicos (en caso de que se requieran)</li>
                </ul>
                <p>Es importante tomar en cuenta que el profesor no recibirá ningún trabajo impreso. Todos los controles de lectura, reseñas, ensayos y demás productos se cargarán en las secciones correspondientes del sitio web, en archivos de Word © o Power Point © según se indique, comprimidos en formato Zip y etiquetados de acuerdo a las instrucciones que aparecen en cada sección.
                <br>
                <br>Para poder realizar las actividades descritas, a cada estudiante se le asignará una <b>cuenta y una contraseña</b> para acceder al sitio desde Internet. La responsabilidad de la administración técnica del sitio estará a cargo del profesor con el apoyo del personal de la División de Informática de la UASLP. Habrá una lista de correo electrónico que funcionará como boletín de avisos del grupo. Por lo anterior, todos los estudiantes deben contar con una cuenta de <b>correo electrónico</b> especial para recibir los <b>mensajes</b> de boletines y los avisos del curso. Los avisos también se publicarán en el tablero de anuncios del sitio web. Es responsabilidad de cada estudiante revisar su buzón de mensajes o visitar el sitio web para verificar estos avisos con suficiente oportunidad.
                <br>
                <br>Como primer paso, después de la primera sesión del curso todos los estudiantes deberán poner sus datos en la sección contactos. Estos datos son muy importantes para efectos de localización e información sobre imprevistos.
                <br>
                <br>Es imprescindible cumplir los siguientes requerimientos de seguridad y cortesía en internet:</p>
                <ol style="list-style-type: lower-roman;">
                    <li>No compartir los nombres de usuario y contraseñas con otras personas.</li>
                    <li>No utilizar las direcciones de correo electrónico, ni la información que aparece en la sección de contactos, con fines diferentes a los del curso, a menos que se tenga autorización explícita.</li>
                    <li>No modificar los parámetros de las secciones “Administración”. Por razones de seguridad todas las actividades de los usuarios quedan registradas. Cualquier intento -fallido o exitoso- de modificar los parámetros o de dañar el sitio deliberadamente y sin autorización, significará la cancelación de las posibilidades de ingreso al sitio web de la persona involucrada y con ello pondrá en riesgo la calificación final del curso.</li>
                    <li>No modificar la configuración y columnas de las diferentes secciones del sitio web (avisos, bibliotecas de documentos, paneles de discusión). Esto puede generar avisos de error y dificultades de navegación.</li>
                    <li>Contar con un programa antivirus confiable y actualizado en la PC donde se descargan los archivos del sitio web, pues no es posible controlar la inexistencia de virus en los archivos.</li>
                    <li>No enviar de cadenas (por más plausibles que parezcan), alertas de virus, copias de boletines, anuncios comerciales y demás mensajes que puedan considerarse SPAM (mensajes no deseados). Esto aplica tanto para las cuentas de correo electrónico y como para las diferentes secciones del sitio web.</li>
                </ol>
